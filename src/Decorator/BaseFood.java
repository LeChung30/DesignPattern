package Decorator;

public class BaseFood implements IFood {
    private String name;
    private double price;

    public BaseFood(String name, double price) {
        this.name = name;
        this.price = price;
    }

    @Override
    public String getName() {
        return this.name;
    }

    @Override
    public double getPrice() {
        return this.price;
    }
}
